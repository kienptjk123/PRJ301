package controller;

import dao.userDAO;
import dto.UserDTO;
import java.io.IOException;
import java.io.PrintWriter;
import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import org.mindrot.jbcrypt.BCrypt;

/**
 *
 * @author adm
 */
@WebServlet(name = "RegisterController", urlPatterns = {"/RegisterController"})
public class RegisterController extends HttpServlet {

    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    private final String SUCCESS = "login.jsp"; // login page
    private final String ERROR = "register.jsp"; // register page

    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        String url = ERROR;
        try {
            String password = request.getParameter("password");
            String confirm_password = request.getParameter("confirmPassword");
            if (password.equals(confirm_password)) {
                String username = request.getParameter("username");
                String email = request.getParameter("email");
                String hashedPassword = BCrypt.hashpw(password, BCrypt.gensalt());
                userDAO dao = new userDAO();
                UserDTO new_user = UserDTO.builder()
                        .username(username)
                        .password(hashedPassword)
                        .email(email)
                        .build();

                boolean result = dao.createAccount(new_user);
                if (result) {
                    request.setAttribute("message", "Registed successful!");
                    url = SUCCESS;
                } else {
                    request.setAttribute("message", "Fail!");
                }
            } else {
                request.setAttribute("message", "Confirm password isn't matched with Password");
            }
        } catch (Exception e) {
            request.setAttribute("message", "Error at REGISTER CONTROLLER");

        } finally {
            request.getRequestDispatcher(url).forward(request, response);
        }

    }

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>
}
